for i in range(0, len(matriz)):
    for j in range(0, len(matriz[i])):
        matriz[i][j] = float(input())
        
match operator :
    case "S":

        sum_diagonal = sum_above_diagonal(matriz)

        print("{:.1f}".format(sum_diagonal))

